package test_ex;

public class OperEx1 {
	public static void main(String args[]){
		
		/*데이터 연산자
		수학적(산술) 연산자
		표현)
			+	=> 더하기
			-	=> 빼기
			*	=> 곱하기
			/	=> 나누기
			%	=> 나머지*/
		
		// ex)
		int a = 10;
		int b = 20;
		int result = 0;
		result = a + b;
		System.out.println("결과 : =>" + a + "+" +  b + "=" + result);
		
		//출력 결과 => 10 % 20 = 10 
			
		
		
		result = a % b;
		System.out.println("결과 : =>" + a + "%" +  b + "=" + result);
		
		
	}

}
